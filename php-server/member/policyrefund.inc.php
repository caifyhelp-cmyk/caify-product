    <h4>1. 청약철회 가능 기간</h4>
    <p class="policyrules__item">회원은 유료 서비스 결제일로부터 7일 이내 관련 법령에 따라 청약철회를 요청할 수 있습니다. 단, 다음의 경우에는 관련 법령이 허용하는 범위 내에서 청약철회가 제한될 수 있습니다.</p>


    <h4>2. 청약철회가 제한되는 경우</h4>
    <p class="policyrules__item">다음 각 호의 어느 하나에 해당하는 경우 청약철회가 제한될 수 있으며, 회사는 청약철회 제한에 해당하는 경우, 결제 과정에서 해당 사실 및 청약철회 제한 가능성을 사전에 명확히 고지하고 이에 대한 회원의 동의를 받습니다.</p>
    <ol class="policyrules__ol">
        <li>회원의 명시적 동의 하에 서비스 제공이 개시된 경우</li>
        <li>맞춤형 세팅, 콘텐츠 기획·제작, 자동화 설정 등 개별 작업이 이미 착수된 경우</li>
        <li>디지털 콘텐츠가 다운로드 또는 열람 가능한 상태가 된 경우</li>
        <li>이용자의 요청에 따라 즉시 이행되는 맞춤형 서비스의 특성상 회복이 불가능한 경우</li>
    </ol>

    <h4>3. 서비스 제공 개시 기준</h4>
    <p class="policyrules__item">본 서비스에서 “서비스 제공 개시”란 다음 중 어느 하나에 해당하는 시점을 의미합니다.</p>
    <ol class="policyrules__ol">
        <li>결제 완료 후 콘텐츠 생성이 시작된 시점</li>
        <li>자동화 세팅 또는 시스템 설정 작업이 착수된 시점</li>
        <li>디지털 콘텐츠가 회원 계정에 제공되어 열람 또는 다운로드 가능한 상태가 된 시점</li>
        <li>구독형 서비스의 경우 해당 결제 주기에 따른 이용 기간이 개시된 시점</li>
    </ol>

    <h4>4. 환불 기준</h4>
    <p class="policyrules__item">서비스가 제공되기 전에는 전액을 환불합니다. 단, 이미 서비스가 제공된 이후라면 이미 제공된 서비스 금액 또는 실제 발생 비용을 공제 후 환불합니다.</p>
    
    <h4>5. 구독형 서비스 환불 기준</h4>
    <p class="policyrules__item">구독형 서비스의 환불금은 다음과 같이 산정됩니다.</p>
    <ol class="policyrules__ol"> 
        <li>환불금 = 결제금액 − (이미 제공된 서비스 수량 × 1건당 단가)</li>
        <li>1건당 단가는 결제 시점의 서비스 안내 화면에 고지된 기준을 따릅니다.</li>
        <li>해당 월 제공 예정 수량을 모두 이용한 경우 해당 월 이용요금은 환불되지 않습니다.</li>
        <li>구독 해지는 다음 결제 예정일 이전까지 마이페이지 또는 고객센터를 통해 신청할 수 있습니다.</li>
        <li>해지 신청이 완료된 경우 다음 결제일부터 자동결제가 이루어지지 않습니다.</li>
    </ol>

    <h4>6. 단건 유료 서비스</h4>
    <p class="policyrules__item">단건 유료 서비스 또한 서비스 제공 전에는 전액 환불합니다. 단, 이미 서비스가 제공된 이후라면 이미 제공된 서비스 금액 또는 실제 발생 비용을 공제 후 환불합니다.</p>

    <h4>7. 회사 사유 환급</h4>
    <p class="policyrules__item">‘회사’의 귀책사유로 서비스 제공이 불가능한 경우, ‘회사’는 해당 사실을 통지한 날로부터 3영업일 이내 환급 조치를 진행합니다.</p>

    <h4>8. 환불 신청 방법</h4>
    <p class="policyrules__item">환불 또는 청약철회 요청은 다음 방법으로 접수할 수 있으며, ‘회사’는 접수일로부터 5영업일 이내 환불 여부를 결정하고 처리합니다.</p>
    <ol class="policyrules__ol"> 
        <li>마이페이지 내 환불 신청 기능</li>
        <li>고객센터 이메일: (이메일 주소 기재)</li>
        <li>고객센터 전화: (전화번호 기재)</li>
    </ol>

    <h4>9. 환불 처리 기간</h4>
    <p class="policyrules__item">
        환불은 승인일로부터 5영업일 이내 결제수단을 통해 처리됩니다. 회사의 사유로 환불이 지연되는 경우, 관련 법령에 따른 법정 지연이자를 지급합니다. <br>
