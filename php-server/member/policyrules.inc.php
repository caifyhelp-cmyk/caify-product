                    <li>나. 계정으로 인해 ‘회사’ 또는 다른 ‘이용자’에게 피해를 줄 수 있는 일체의 행위</li>
                </ul>                 
            </li>
            <li>명의 사칭, 도용 등
                <ul>
                    <li>가. 회원이 다른 회원, 제3자 또는 ‘회사’의 직원을 사칭하는 행위</li>
                    <li>나. 의도적이거나 특정한 목적을 가지고 제3자의 명의를 사칭하거나 도용하는 일체의 행위</li>
                </ul>                
            </li>
            <li>기타 
                <ul>
                    <li>가. 위 각 행위 외 ‘회사’가 정한 제한사항을 위반하는 행위</li>
                    <li>나. 본인 또는 타인에게 재산상의 이익을 주거나 타인에게 손해를 가할 목적으로 허위·과장 정보를 확산시키는 행위</li>
                    <li>다. 기타 관련 법령에서 금지하거나 선량한 풍속 기타 사회통념상 허용되지 아니하거나 원활한 서비스 제공에 악영향을 끼치는 일체의 행위</li>
