    <h4>제21조(면책 사항)</h4>
    <ul class="policyrules__item">
        <li>① ‘회사’는 다음 각 호의 해당하는 경우에는 면책됩니다.</li>
        <ol>
            <li>전시, 천재지변, 국가비상사태, 자연재해 등 ‘회사’에서 해결이 불가능한 곤란한 기술적 결함 기타 불가항력적 사유로 서비스를 제공할 수 없는 경우</li>
            <li>회원의 귀책사유로 인한 서비스의 중지, 이용 장애 및 서비스 이용계약이 해지된 경우</li>
            <li>기간통신 사업자가 전기통신서비스를 중지하거나 정상적으로 제공하지 아니한 경우</li>
            <li>사전에 공지된 ‘서비스’와 관련된 설비의 보수, 교체, 정기점검, 공사 등 부득이한 사유로 ‘서비스’가 중지되거나 장애가 발생한 경우</li>
            <li>회원의 컴퓨터 환경으로 인하여 발생하는 일체의 문제 또는 ‘회사’의 고의 또는 중대한 과실이 없는 네트워크 환경으로 발생하는 경우</li>
            <li>‘서비스’ 관련 협력사의 설비 장애 등 협력사 관련 사유로 ‘서비스’ 제공이 중지되거나 장애가 발생한 경우</li>
            <li>‘회사’의 고의 또는 중대한 과실 없이 회원 상호간 또는 회원과 제3자간에 서비스를 매개로 분쟁이 발생한 경우</li>
            <li>회원의 기기 오류에 의한 손해가 발생한 경우 또는 신상정보 및 전자우편주소를 부정확하게 기재하거나 미 기재하여 손해가 발생한 경우</li>
            <li>관련 법령, 정부 정책 등에 따라 서비스를 제공할 수 없는 경우</li>
            <li>무료로 제공되는 서비스 이용</li>
            <li>‘회사’의 고의 또는 중대한 과실로 인한 경우를 제외한 회원이 계정 비밀번호, 각종 기기 비밀번호, 등의 보안을 철저하게 하지 않아 회원정보의 유출이 발생한 경우</li>
