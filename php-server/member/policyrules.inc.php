    </ul>

    <h4>제22조(저작권의 귀속 및 이용제한)</h4>
    <ul class="policyrules__item">
        <li>① ‘회사’가 제공한 산출물에 대한 저작권 및 기타 지적재산권은 ‘회사’에 귀속합니다.</li>

        <li>② ‘이용자’는 ‘서비스’를 이용함으로써 얻은 정보 중 ‘회사’에게 지적재산권이 귀속된 정보를 ‘회사’의 사전 승낙 없이 복제, 송신, 출판, 배포, 방송 기타 방법에 의하여 영리목적으로 이용하거나 제3자에게 이용하게 해서는 안됩니다.</li>

        <li>③ ‘회사’는 약정에 따라 ‘이용자’에게 귀속된 저작권을 사용하는 경우 당해 ‘이용자’에게 통보합니다.</li>
    </ul>

    <h4>제23조(분쟁해결)</h4>
    <ul class="policyrules__item">
        <li>① ‘회사’는 ‘이용자’가 제기하는 정당한 의견이나 불만을 확인한 즉시 반영하고 그 피해를 보상처리하기 위하여 피해보상처리기구를 설치 및 운영합니다.</li>

        <li>② ‘회사’는 ‘이용자’로부터 제출되는 불만사항 및 의견은 우선적으로 그 사항을 처리합니다. 단, 신속한 처리가 곤란한 경우에는 ‘이용자’에게 그 사유와 처리일정을 즉시 통보합니다.</li>

        <li>③ ‘회사’와 ‘이용자’ 간에 발생한 전자상거래 분쟁과 관련하여 ‘이용자’의 피해구제신청이 있는 경우, 공정거래위원회 또는 시·도지사가 의뢰하는 분쟁조정기관의 조정에 따를 수 있습니다.</li>

        <li>④ ‘회사’는 서비스와 관련하여 ‘회사’의 고의 또는 중대한 과실에 의한 손해를 제외하고 어떠한 책임도 부담하지 않으며, 이로 인한 분쟁 발생 시 제23조 4항에 의거해 분쟁해결을 위한 행위에 있어 정당함을 갖습니다.</li>
    </ul>


    <h4>제24조(재판권 및 준거법)</h4>
    <ul class="policyrules__item">
        <li>① ‘회사’와 ‘이용자’ 간에 발생한 전자상거래 분쟁에 관한 소송은 제소 당시의 ‘이용자’의 주소에 의하고, 주소가 없는 경우에는 거소를 관할하는 지방법원의 전속관할로 합니다. 단, 제소 당시 ‘이용자’의 주소 또는 거소가 분명하지 않거나 외국 거주자의 경우에는 민사소송법상의 관할법원에 제기합니다.</li>
        <li>② ‘회사’와 ‘이용자’ 간에 제기된 전자상거래 소송에는 한국법을 적용합니다.</li>

    </ul>

 
