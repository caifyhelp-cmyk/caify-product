                    <li>나. 타인의 개인정보를 침해하는 행위 </li>
                    <li>다. 기타 웹 해킹 프로그램, 매크로 프로그램, 보이스 피싱을 위한 미러링 사이트 등 타인의 권리를 침해하거나 침해할 우려가 있는 모든 행위</li>
