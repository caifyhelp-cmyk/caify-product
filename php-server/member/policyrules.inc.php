                </ul>  
            </li>
            <li>허위 또는 과장된 정보 입력 
                <ul>
                    <li>가. 허위 또는 과장된 정보를 입력하는 행위
                    <li>나. 의도적이거나 특정한 목적을 가지고 경쟁사의 정보를 사용해 경쟁사에 대한 부정적인 인식을 줄 수 있는 행위</li>
                </ul>
            </li>
