                    <li>나. ‘회사’에서 제공하는 산출물을 통한 ‘이용자’의 사용 외, ‘회사’의 동의 없이 영리, 영업, 광고, 정치활동 등을 목적으로 ‘서비스’를 사용하는 행위 </li>
