        <li>위탁되는 개인정보 항목 : 결제에 필요한 정보(결제수단별 결제 정보 등) 및 거래정보 결제수단별 상세 항목은 토스페이먼츠 정책에 따름</li>
        <li>보유·이용기간 : 위탁계약 종료 시까지(단, 법령상 보관 의무가 있는 경우 해당 기간까지)</li>
    </ol>


