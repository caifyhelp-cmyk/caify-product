    </p>

    <h4>제1조(개인정보의 처리 목적)</h4>
    <p class="policyrules__item">‘회사’는 다음 목적을 위해 개인정보를 처리합니다. 처리한 개인정보는 아래 목적 이외의 용도로 이용되지 않으며, 목적 변경 시 관련 법령에 따라 사전 동의를 받거나 필요한 조치를 이행합니다.</p>
    <ol class="policyrules__ol">
        <li>회원 가입 및 계정 관리: 회원가입 의사 확인, 본인 식별·인증, 계정 생성·유지·관리, 부정이용 방지, 공지·안내사항 전달</li>
        <li>서비스 제공(디지털 콘텐츠 생성 및 업무 자동화): 서비스 이용을 위한 고객사 정보 등록·관리, AI 콘텐츠 생성/업무 자동화 기능 제공, 결과물 제공, 운영·기술 지원</li>
        <li>계약 및 결제·정산: 유료 서비스 결제, 결제 내역 확인, 환불 처리, 세금계산서/현금영수증 발급(해당 시)</li>
        <li>고객 문의·민원 처리: 문의자 식별, 문의/민원 처리, 사실 확인 및 처리 결과 안내</li>
        <li>마케팅 및 광고(선택 동의 시): 이벤트/프로모션 안내, 뉴스레터 발송, 맞춤형 안내(수신 동의 철회 시 즉시 중단)</li>
    </ol>

