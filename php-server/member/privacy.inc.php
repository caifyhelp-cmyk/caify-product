    <p class="policyrules__item">‘회사’는 원활한 서비스 제공을 위해 다음과 같이 개인정보 처리업무를 위탁할 수 있으며, 관련 법령에 따라 수탁자가 개인정보를 안전하게 처리하도록 관리·감독합니다.</p>
    <ol class="policyrules__ol">
